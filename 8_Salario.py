#8 Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre o total do seu salário no referido mês.

salariohora = float(input("Digite quanto você ganha por hora: "))
numhora = int(input("Digite o número de horas que você trabalha por mês: "))
salariototal = salariohora * numhora
print("O total do seu salário é ", salariototal)
